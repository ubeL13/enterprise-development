namespace BikeRental.Domain.Enums;

/// <summary>
/// Enumeration of available bike types.
/// </summary>
public enum BikeType
{
    /// <summary>
    /// Road bike — designed for speed on paved roads.
    /// </summary>
    Road,

    /// <summary>
    /// Mountain bike — suitable for off-road riding.
    /// </summary>
    Mountain,

    /// <summary>
    /// Hybryd bike - designed primarily for paved roads and light off-road terrain, prioritising comfort and versatility
    /// </summary>
    Hybrid,

    /// <summary>
    /// BMX bike - for performing tricks using trampolines
    /// </summary>
    Bmx,

    /// <summary>
    /// Electric bike — equipped with an electric motor assist.
    /// </summary>
    Electric,

    /// <summary>
    /// City bike — designed for driving in the city, on paved roads.
    /// </summary>
    City,

    /// <summary>
    /// Sport bike — a motorcycle optimized for speed, acceleration, braking, and maneuverability on race tracks.
    /// </summary>
    Sport
}
